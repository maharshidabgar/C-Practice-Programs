#include <stdio.h>
                        // WhileLoop Syntax Example
int main() {

    int i = 1;     // Initialization of Variable - Starting of Condition & Outside the Loop

    while (i<=10) {     // Termination of Variable - Stoping of condition / Termination

        printf("%d \n", i);
        i++;             // Updation of Variable - Incr/Decr with PostIncr
        
    }

    return 0;

}